    {
        //validate the data very firt
       $this->validate(request(),['username'=>'required|min:5','email'=>'required|email','password'=>'required|confirmed']);
        //create the user

        $user= User::create
        (
            [

               'name'=>request('username'),
                'email'=>request('email'),
                'password'=>bcrypt(request('password'))


            ]



    );


        //signed them in
        Auth::login($user);





        //redirect to the desired page
        return view('hotel-profile4');
    }

